// An example of character-int conversions
#include <stdio.h>

int main()
{
    char ch = '0';     // the character zero
    int number = 0;    // the number zero
    printf("Character zero = %c \n", ch);
    printf("Ascii code of character zero = %d \n", ch);
    printf("Integer zero = %d \n", number);

    return 0;
}
